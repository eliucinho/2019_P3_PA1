    public Humanos getHumanoFormulario(){
        String identidad=txtIdentidad.getText();
        String nombre=txtNombre.getText();
        String apellido=txtApellido.getText();
        String anioNacimiento=txtAnioNacimiento.getText();
        String direccion=txtDireccion.getText();
        
        Humanos humano=new Humanos();
        humano.setIdentidad(identidad);
        humano.setNombre(nombre);
        humano.setApellido(apellido);
        humano.setAnioNacimiento(Integer.parseInt(anioNacimiento));
        humano.setDireccion(direccion);
        
        return humano;
    }
    
    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        Humanos nuevoHumano=getHumanoFormulario();
        gestorJPA.insertarHumano(nuevoHumano);
        JOptionPane.showMessageDialog(this, "Registro Nuevo Almacenado");
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        gestorJPA.eliminarHumano(getHumanoFormulario());
        
        JOptionPane.showMessageDialog(this, "Registro Eliminado");
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String identidad=txtIdentidad.getText();
        String nombre=txtNombre.getText();
        String apellido=txtApellido.getText();
        int anioNacimiento
                = Integer.valueOf(
                        txtAnioNacimiento.getText()
                );
        
        //seleccion de los componentes de opcion
        String sexo="M";
        if (optFemenino.isSelected()) {
            sexo="F";
        }
        String departamento=
                cmbDepartamentos
                        .getSelectedItem()
                        .toString();
        String municipio=
                lstMunucipios
                        .getSelectedValue()
                        .toString();
        
        Persona newPersona=
                new Persona(identidad
                        , nombre
                        , apellido
                        , anioNacimiento
                        , sexo
                        , departamento
                        , municipio);
        
        controladorPersona
                .setPersonaActual(newPersona);
        
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

